public class demo {

     public static void main(String[] args) {

         int a = 5;
         for (int i = 1; i <= a; i++) {
             if (i % 2 == 0)
                 System.out.print("  * *");
             else
                 System.out.print("* *");
             System.out.println(" ");
         }
     }
 } 